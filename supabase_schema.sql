-- 1. Sucursales (sin dependencias, debe ir primero)
CREATE TABLE public.branches (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    code TEXT UNIQUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Tabla de usuarios (depende de branches y auth.users)
CREATE TABLE public.users (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT NOT NULL,
    full_name TEXT,
    role TEXT NOT NULL DEFAULT 'planta' CHECK (role IN ('admin', 'planta', 'sucursal', 'chofer')),
    branch_id BIGINT REFERENCES public.branches(id),
    phone TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Trigger para actualizar updated_at
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_users_updated_at
    BEFORE UPDATE ON public.users
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- 3. Choferes
CREATE TABLE public.drivers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    rut TEXT UNIQUE NOT NULL,
    phone TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 4. Camiones
CREATE TABLE public.trucks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plate TEXT UNIQUE NOT NULL,
    type TEXT NOT NULL, -- 'carga' o 'descarga'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. Programaciones (depende de branches y trucks)
CREATE TABLE public.schedules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    branch_id BIGINT NOT NULL REFERENCES public.branches(id),
    scheduled_date DATE NOT NULL,
    truck_id BIGINT REFERENCES public.trucks(id),
    status TEXT NOT NULL DEFAULT 'pending',
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 6. Envios/Shipments - Flujo principal (depende de trucks, drivers, branches)
CREATE TABLE public.shipments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    truck_id BIGINT NOT NULL REFERENCES public.trucks(id),
    driver_id BIGINT NOT NULL REFERENCES public.drivers(id),
    branch_id BIGINT NOT NULL REFERENCES public.branches(id),

    status TEXT NOT NULL, -- 'esperaIngreso', 'enEspera', 'enCarga', 'cargado', 'despachado', 'recibido', 'incidencia'

    arrival_time TIMESTAMP WITH TIME ZONE,
    load_start TIMESTAMP WITH TIME ZONE,
    load_end TIMESTAMP WITH TIME ZONE,
    dispatch_time TIMESTAMP WITH TIME ZONE,
    reception_time TIMESTAMP WITH TIME ZONE,

    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 7. Fotos de Carga (depende de shipments)
CREATE TABLE public.load_photos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id BIGINT NOT NULL REFERENCES public.shipments(id) ON DELETE CASCADE,
    image_url TEXT NOT NULL,
    comment TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 8. Incidencias (depende de shipments y users)
CREATE TABLE public.incidents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id BIGINT NOT NULL REFERENCES public.shipments(id) ON DELETE CASCADE,
    type TEXT NOT NULL,
    description TEXT NOT NULL,
    photo_url TEXT,
    status TEXT NOT NULL DEFAULT 'abierta' CHECK (status IN ('abierta', 'en_proceso', 'resuelta')),
    resolution TEXT,
    reported_by UUID REFERENCES public.users(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    resolved_at TIMESTAMP WITH TIME ZONE
);

-- 9. Historial de cambios de estado - Auditoria (depende de shipments y users)
CREATE TABLE public.shipment_status_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id BIGINT NOT NULL REFERENCES public.shipments(id) ON DELETE CASCADE,
    old_status TEXT,
    new_status TEXT NOT NULL,
    changed_by UUID REFERENCES public.users(id),
    notes TEXT,
    changed_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

