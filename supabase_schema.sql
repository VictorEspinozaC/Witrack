-- Users: pueden ver/editar su propio perfil
CREATE POLICY "Users can view own profile" ON public.users
    FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Users can update own profile" ON public.users
    FOR UPDATE USING (auth.uid() = id);

CREATE POLICY "Enable insert for authenticated users" ON public.users
    FOR INSERT WITH CHECK (auth.uid() = id);

-- Admin puede ver todos los usuarios
CREATE POLICY "Admins can view all users" ON public.users
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM public.users u
            WHERE u.id = auth.uid() AND u.role = 'admin'
        )
    );

-- Branches: lectura para todos los autenticados, escritura para admins
CREATE POLICY "Authenticated users can view branches" ON public.branches
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Authenticated users can insert branches" ON public.branches
    FOR INSERT WITH CHECK (auth.role() = 'authenticated');

CREATE POLICY "Authenticated users can update branches" ON public.branches
    FOR UPDATE USING (auth.role() = 'authenticated');

-- Drivers, Trucks, Schedules, Shipments, etc: acceso completo para autenticados
CREATE POLICY "Authenticated users full access" ON public.drivers
    FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Authenticated users full access" ON public.trucks
    FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Authenticated users full access" ON public.schedules
    FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Authenticated users full access" ON public.shipments
